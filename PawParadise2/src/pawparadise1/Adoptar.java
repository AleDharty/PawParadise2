    public void cargarMascotas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Raza");
        modelo.addColumn("Edad");
        modelo.addColumn("Sexo");
        modelo.addColumn("Esterilizado");
        modelo.addColumn("Historia");
        modelo.addColumn("Tiempo");
        modelo.addColumn("Vacunas");

        tablaMascotas.setModel(modelo);

        Conexion con = new Conexion("pawparadise"); // Asegúrate que tu BD se llama así
        Connection conn = con.conectar();

        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM mascotas");

            while (rs.next()) {
                Object[] fila = new Object[9];
                fila[0] = rs.getInt("id");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("raza");
                fila[3] = rs.getString("edad");
                fila[4] = rs.getString("sexo");
                fila[5] = rs.getString("esterilizado");
                fila[6] = rs.getString("historia");
                fila[7] = rs.getString("tiempo");
                fila[8] = rs.getString("vacunas");

                modelo.addRow(fila);
            }

            rs.close();
            st.close();
            con.desconectar();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos: " + ex.getMessage());
        }
    }
