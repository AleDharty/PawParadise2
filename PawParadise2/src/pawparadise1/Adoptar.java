    private void btnAdoptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdoptarActionPerformed
        int filaSeleccionada = tablaMascotas.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un peludito para adoptar.");
            return;
        }

        // Opcional: puedes obtener más datos de la mascota si los necesitas
        int idMascota = (int) tablaMascotas.getValueAt(filaSeleccionada, 0);
        String nombreMascota = (String) tablaMascotas.getValueAt(filaSeleccionada, 1);

        // Abrir formulario de adopción
        FormularioAdopcion formulario = new FormularioAdopcion(idMascota, nombreMascota);
        formulario.setVisible(true);
    }//GEN-LAST:event_btnAdoptarActionPerformed
    
