    private void SubirFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubirFotoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Selecciona una imagen");

        // Solo permite im치genes
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.addChoosableFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Im치genes", "jpg", "png", "jpeg", "gif"));

        int seleccion = fileChooser.showOpenDialog(this);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            java.io.File archivo = fileChooser.getSelectedFile();
            String ruta = archivo.getAbsolutePath();

            // Escalar la imagen al tama침o del JLabel
            ImageIcon imagenOriginal = new ImageIcon(ruta);
            Image imagenEscalada = imagenOriginal.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_SMOOTH);

            lblFoto.setIcon(new ImageIcon(imagenEscalada));
            lblFoto.setText("");

        }
    }//GEN-LAST:event_SubirFotoActionPerformed

