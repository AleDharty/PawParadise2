    private void btnEnviarBD1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarBD1ActionPerformed
    Conexion con = new Conexion("pawparadise");
        Connection conn = con.conectar();

        if (conn != null) {
            try {
                String sql = "INSERT INTO mascotas (id,nombre, raza, edad, sexo, esterilizado, historia, tiempo, vacunas) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement ps = conn.prepareStatement(sql);

                // Recolectar datos de los campos
                ps.setString(1, Jid.getText());
                ps.setString(2, Jnombres.getText());
                ps.setString(3, Jraza.getText());
                ps.setString(4, Jedad.getText());
                ps.setString(5, Jsexo1.getSelectedItem().toString());
                ps.setString(6, jComboBoxEsterilizado.getSelectedItem().toString());
                ps.setString(7, JHisotria.getText());
                ps.setString(8, Jtiempo.getText());
                ps.setString(9, Jvacunas.getText());

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Peludito registrado con 칠xito 游");

                // Limpiar campos

                Jid.setText("");
                Jnombres.setText("");
                Jraza.setText("");
                Jedad.setText("");
                Jsexo1.setSelectedIndex(0);
                jComboBoxEsterilizado.setSelectedIndex(0);
                JHisotria.setText("");
                Jtiempo.setText("");
                Jvacunas.setText("");
                lblFoto.setIcon(null);
                lblFoto.setText("      Adjunta la foto del peludito aqu칤");

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al guardar: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo conectar con la base de datos.");
        }
    }//GEN-LAST:event_btnEnviarBD1ActionPerformed

